package server.widgets.bean;

public class Dependencies {

	private boolean jquery;
	private boolean bootstrap;
	private boolean facebook;
	private boolean linkedin;
	private boolean twitter;
	private boolean googlemap;
	private boolean googleplus;
	
	
	public boolean isJquery() {
		return jquery;
	}
	public void setJquery(boolean jquery) {
		this.jquery = jquery;
	}
	public boolean isBootstrap() {
		return bootstrap;
	}
	public void setBootstrap(boolean bootstrap) {
		this.bootstrap = bootstrap;
	}
	public boolean isFacebook() {
		return facebook;
	}
	public void setFacebook(boolean facebook) {
		this.facebook = facebook;
	}
	public boolean isLinkedin() {
		return linkedin;
	}
	public void setLinkedin(boolean linkedin) {
		this.linkedin = linkedin;
	}
	public boolean isTwitter() {
		return twitter;
	}
	public void setTwitter(boolean twitter) {
		this.twitter = twitter;
	}
	public boolean isGooglemap() {
		return googlemap;
	}
	public void setGooglemap(boolean googlemap) {
		this.googlemap = googlemap;
	}
	public boolean isGoogleplus() {
		return googleplus;
	}
	public void setGoogleplus(boolean googleplus) {
		this.googleplus = googleplus;
	}
	
	
}
